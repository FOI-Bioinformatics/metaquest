def _generate_html_report(
    title: str,
    summary_data: pd.DataFrame,
    metadata_data: Optional[pd.DataFrame],
    counts_data: Optional[pd.DataFrame],
    output_file: Union[str, Path],
    threshold: float,
    include_plots: bool,
    include_tables: bool,
) -> Path:
    """
    Generate an HTML report from analysis results.

    Args:
        title: Title for the report
        summary_data: Containment summary DataFrame
        metadata_data: Metadata DataFrame
        counts_data: Metadata counts DataFrame
        output_file: Path to save the report
        threshold: Containment threshold for filtering
        include_plots: Whether to include plots
        include_tables: Whether to include tables

    Returns:
        Path to the generated HTML report
    """
    if not JINJA2_AVAILABLE:
        raise VisualizationError("HTML report generation requires jinja2. " "Please install with 'pip install jinja2'")

    # Create output directory
    output_path = Path(output_file)
    report_dir = output_path.parent
    report_dir.mkdir(parents=True, exist_ok=True)

    # Create images directory
    images_dir = report_dir / "images"
    images_dir.mkdir(exist_ok=True)

    # Generate plots and save to images directory
    plot_files = {}

    if include_plots:
        plot_files = _generate_plots_for_html(summary_data, counts_data, threshold, images_dir)

    # Prepare data for template
    template_data = _prepare_template_data(
        title,
        summary_data,
        metadata_data,
        counts_data,
        include_plots,
        include_tables,
        threshold,
        plot_files,
    )

    # Create or get template path
    _create_default_template()

    # Create Jinja2 template
    template_loader = jinja2.FileSystemLoader(searchpath=str(Path(__file__).parent))
    template_env = jinja2.Environment(loader=template_loader)

    # Get template
    template = template_env.get_template("templates/report_template.html")

    # Render template
    output_html = template.render(**template_data)

    # Write to file
    with open(output_path, "w") as f:
        f.write(output_html)

    logger.info(f"HTML report saved to {output_path}")
    return output_path
